static float quadInterpolation(
    float a0[2], float a1[2], float a2[2], float f)
{
    float af0x = f - a0[0];
    float af1x = f - a1[0];
    float af2x = f - a2[0];
    float a01x = a0[0] - a1[0];
    float a02x = a0[0] - a2[0];
    float a12x = a1[0] - a2[0];
    float t0 = af1x / a02x;
    float t1 = af2x / a01x;
    float t2 = af0x / a12x;
    return t1 * (a0[1] * t0 - a1[1] * t2) + a2[1] * t2 * t0;
}

